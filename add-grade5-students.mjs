async function addGrade5Students() {
  console.log("بدء إضافة طلاب الصف الخامس...");
  
  for (const studentName of grade5Students) {
    try {
      await db.insert(students).values({
        fullName: studentName,
        grade: "الخامس الابتدائي",
        score: 0,
      });
      console.log(`✓ تم إضافة: ${studentName}`);
    } catch (error) {
      console.error(`✗ خطأ في إضافة ${studentName}:`, error.message);
    }
  }
  
  console.log(`\nتم الانتهاء! تم إضافة ${grade5Students.length} طالب للصف الخامس الابتدائي`);
  process.exit(0);
}

