async function addGrade4Students() {
  const db = drizzle(process.env.DATABASE_URL);
  
  console.log("بدء إضافة طلاب الصف الرابع الابتدائي...");
  console.log(`عدد الطلاب: ${grade4Students.length}`);
  
  for (let i = 0; i < grade4Students.length; i++) {
    const score = Math.floor(Math.random() * 100) + 1; // نقاط عشوائية من 1 إلى 100
    
    await db.insert(students).values({
      fullName: grade4Students[i],
      score: score,
    });
    
    console.log(`✓ [${i + 1}/${grade4Students.length}] ${grade4Students[i]} - النقاط: ${score}`);
  }
  
  console.log(`\n✅ تم إضافة ${grade4Students.length} طالب من الصف الرابع بنجاح!`);
  process.exit(0);
}

addGrade4Students().catch((error) => {
  console.error("❌ حدث خطأ:", error);
  process.exit(1);
});
