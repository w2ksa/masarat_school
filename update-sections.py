def update_section(names, grade, section):
    """تحديث رقم الفصل لمجموعة من الطلاب"""
    count = 0
    for name in names:
        try:
            cursor.execute(
                "UPDATE students SET section = %s WHERE fullName = %s AND grade = %s",
                (section, name, grade)
            )
            if cursor.rowcount > 0:
                count += 1
        except Exception as e:
            print(f"❌ خطأ في تحديث {name}: {e}")
    return count

